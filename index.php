                <h2>Características</h2>

                 <p>O vôlei, ou voleibol, é um esporte coletivo que envolve duas equipes separadas por uma rede, cujo objetivo principal é fazer a bola tocar o chão da quadra adversária. Criado em 1895 por William G. Morgan, o esporte combina habilidades como força, agilidade, coordenação e trabalho em equipe, sendo praticado em diversos países ao redor do mundo. Ele pode ser jogado tanto em ambientes fechados quanto ao ar livre, como na modalidade de vôlei de praia.</p>

    <p>Uma das principais características do vôlei é a sua dinâmica de jogo, que exige reflexos rápidos e estratégias bem definidas. Cada equipe é composta por seis jogadores em quadra, que se revezam em posições específicas, como levantador, atacante e líbero. O jogo é dividido em sets, e vence a equipe que alcançar primeiro 25 pontos, com uma diferença mínima de dois pontos em relação ao adversário.</p>

    <p>Além disso, o vôlei é conhecido por suas regras específicas, como o limite de três toques por equipe antes de enviar a bola para o lado adversário. Esses toques geralmente incluem o passe, o levantamento e o ataque, que são executados de forma coordenada para maximizar as chances de pontuar. A comunicação entre os jogadores é essencial para o sucesso da equipe, tornando o esporte um excelente exemplo de cooperação e trabalho em grupo.</p>

    <p>Por fim, o vôlei é um esporte inclusivo e acessível, que pode ser praticado por pessoas de todas as idades e níveis de habilidade. Ele promove benefícios físicos, como o desenvolvimento muscular e cardiovascular, além de contribuir para o bem-estar mental, ao incentivar a socialização e o espírito esportivo. Não é à toa que o vôlei é um dos esportes mais populares e admirados em competições internacionais, como os Jogos Olímpicos.</p>


